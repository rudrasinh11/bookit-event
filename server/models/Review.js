const mongoose = require('mongoose');

const reviewSchema = new mongoose.Schema({
    eventId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Event',
        required: true
    },
    userId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    userName: {
        type: String,
        required: true
    },
    rating: {
        type: Number,
        required: true,
        min: 1,
        max: 5
    },
    comment: {
        type: String,
        required: true,
        trim: true
    }
}, { timestamps: true });

// Prevent a user from leaving multiple reviews on the exact same event
reviewSchema.index({ eventId: 1, userId: 1 }, { unique: true });

module.exports = mongoose.model('Review', reviewSchema);